
package atividade07.classes;


public class Energia {
private double KW;
private String TipoConsumo;
private double precoPorKW;

    public double getKW() {
        return KW;
    }

    public void setKW(double KW) {
        this.KW = KW;
    }

    public String getTipoConsumo() {
        return TipoConsumo;
    }

    public void setTipoConsumo(String TipoConsumo) {
        this.TipoConsumo = TipoConsumo;
    }

    public double getPrecoPorKW() {
        return precoPorKW;
    }

    public void setPrecoPorKW(double precoPorKW) {
        this.precoPorKW = precoPorKW;
    }

    

}
